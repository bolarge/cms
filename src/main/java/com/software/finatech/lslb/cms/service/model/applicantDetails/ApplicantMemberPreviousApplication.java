package com.software.finatech.lslb.cms.service.model.applicantDetails;

public class ApplicantMemberPreviousApplication {
    private String applicantMemberName;
    private String operatorName;
    private String dateOfLicense;

    public String getApplicantMemberName() {
        return applicantMemberName;
    }

    public void setApplicantMemberName(String applicantMemberName) {
        this.applicantMemberName = applicantMemberName;
    }

    public String getOperatorName() {
        return operatorName;
    }

    public void setOperatorName(String operatorName) {
        this.operatorName = operatorName;
    }

    public String getDateOfLicense() {
        return dateOfLicense;
    }

    public void setDateOfLicense(String dateOfLicense) {
        this.dateOfLicense = dateOfLicense;
    }
}
